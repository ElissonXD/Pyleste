        for coluna in range(colunas):
            # Calcula as coordenadas da célula
            x = coluna * cell_width
            y = linha * cell_height
            # Desenha o retângulo na tela
            pygame.draw.rect(tela, (255, 255, 0), pygame.Rect(x, y, cell_width, cell_height), 1)

